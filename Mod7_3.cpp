#include <iostream>

int main() {
    for (int i = 0; i <= 10; i++) {
        std::cout << "Number: " << i << std::endl;
        std::cout << "Multiplied by 2: " << i * 2 << std::endl;
        std::cout << "Multiplied by 10: " << i * 10 << std::endl;
        std::cout << std::endl;
    }
    return 0;
}
